	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPhonekey() {
		return phonekey;
	}
	public void setPhonekey(String phonekey) {
		this.phonekey = phonekey;
	}
