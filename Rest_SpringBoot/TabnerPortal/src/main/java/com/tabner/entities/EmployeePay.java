package com.tabner.entities;

public class EmployeePay {
	
	private String emp_id;
	private double pay;
	private double ins_pay;
	private double final_pay;
	
	public String getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}
	public double getPay() {
		return pay;
	}
	public void setPay(double pay) {
		this.pay = pay;
	}
	public double getIns_pay() {
		return ins_pay;
	}
	public void setIns_pay(double ins_pay) {
		this.ins_pay = ins_pay;
	}
	public double getFinal_pay() {
		return final_pay;
	}
	public void setFinal_pay(double final_pay) {
		this.final_pay = final_pay;
	}
	
	
	
	
	
	
}
