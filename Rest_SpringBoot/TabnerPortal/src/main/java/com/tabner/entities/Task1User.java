package com.tabner.entities;

public class Task1User {

	private String username;
	private String password;
	private String ver_key;
	private String verified;
	private String secret_key;
	private String tfa;
	private String first_name;
	private String last_name;
	private String phone;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getVer_key() {
		return ver_key;
	}
	public void setVer_key(String ver_key) {
		this.ver_key = ver_key;
	}
	public String getVerified() {
		return verified;
	}
	public void setVerified(String verified) {
		this.verified = verified;
	}
	public String getSecret_key() {
		return secret_key;
	}
	public void setSecret_key(String secret_key) {
		this.secret_key = secret_key;
	}
	public String getTfa() {
		return tfa;
	}
	public void setTfa(String tfa) {
		this.tfa = tfa;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
}
