package com.tabner.entities;

public class EmployerTaxes {
	
	private String name;
	private String empId;
	private String paydate;
	private String fui;
	private String meder;
	private String socer;
	private String state;
	private String amount;
	private String addt;
	
	public String getPaydate() {
		return paydate;
	}
	public void setPaydate(String paydate) {
		this.paydate = paydate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getFui() {
		return fui;
	}
	public void setFui(String fui) {
		this.fui = fui;
	}
	public String getMeder() {
		return meder;
	}
	public void setMeder(String meder) {
		this.meder = meder;
	}
	public String getSocer() {
		return socer;
	}
	public void setSocer(String socer) {
		this.socer = socer;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getAddt() {
		return addt;
	}
	public void setAddt(String addt) {
		this.addt = addt;
	}
	
	

}
